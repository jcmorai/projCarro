package uri;



public class Uri911 {

    public static void main(String[] args) {

        int cont;

        cont = 0;

        while (cont <= 100) {
            System.out.print(cont + ", ");
            cont++;
        }
        
        while (cont >= 0) {
            System.out.print(cont + ", ");
            cont--;
        }
        System.out.println("Fim do Programa");

    }
}